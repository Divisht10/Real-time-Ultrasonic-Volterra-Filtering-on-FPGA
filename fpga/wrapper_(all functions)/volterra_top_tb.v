//  (RData, Trans, Receive, H_par_scaled, reborn) are available.
//
//  Usage from the MATLAB command window:
//      gen_tb_vectors(RData, Trans, Receive, H_par_scaled, reborn)
//
//  Outputs (written to current MATLAB working directory):
//      rf_input.mem    229376 lines of 8 hex chars  (Q2.30 RF data, sample-major)
//      ref_env22.mem   124544 lines of 8 hex chars  (Q2.30 expected quad env)
//      ref_env23.mem   124544 lines of 8 hex chars  (Q2.30 expected cubic env)
